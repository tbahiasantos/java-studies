public class Lance {
    
    private Pessoa pessoa;
    private double valor;

    public Lance() {
        pessoa = new Pessoa();
        valor = 0.0;
    }

    public Pessoa getPessoa() {
        return pessoa;
    }

    public void setPessoa(Pessoa pessoa) {
        this.pessoa = pessoa;
    }

    public double getValor() {
        return valor;
    }

    public void setValor(double valor) {
        this.valor = valor;
    }
    
}
